package com.example.leon6.fint;

public class MissionList {

    String ID;
    String title;
    String writer;

    public void setID(String ID) {
        this.ID = ID;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setWriter(String writer) {
        this.writer = writer;
    }

    public String getID() {
        return ID;
    }

    public String getTitle() {
        return title;
    }

    public String getWriter() {
        return writer;
    }

}
